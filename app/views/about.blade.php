    <h1 class="canevalee centeredText">About Olde English Bulldogges</h1>
    <p class="centeredText">
        Olde English Bulldogges are NOT the same as the AKC bulldogs. Olde English Bulldogges are for bulldog lovers who wish their bulldogs could do just a little more. The Olde English Bulldogge is a recreation of the bulldogge that originated in England between 1600 and 1700, that was used primarily in the sport of bull-baiting. For this purpose these dogs where built for functionality that contributed to the necessary physical stamina and endurance which allowed them to participate in such sports. 
    </p>
    <p class="centeredText">
        Around 1835, laws were passed in England banning the sport of bull-baiting and, as a result, the Bulldogges main purpose of existence vanished. Within a decade the numbers of Bulldogges declined drastically almost to extinction. Dog show fanciers took an interest in the breed, but they were in pursuit of a more aesthetically pleasing bulldog for the show ring. They crossed the remnants of the existing stock with the pug and over the years that followed they developed the modern day English Bulldog. Unfortunately, the modern day English Bulldog is wrought with genetic health problems. The shortened muzzle and spine combined with overall more compact structure of the English Bulldog contributes to the many health disorders that are prevalent within the modern AKC English Bulldog breed. Please do your research before you buy one.
    </p>
